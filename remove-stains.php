                <div class="big-block">
                    <p><strong>Here you go...</strong></p>
                    <p class="black-text"><strong>Alcohol</strong></p>
                    <p>Stock up on Baking Soda. It will soon be your best friend and the go-to item in your cupboard, as it gets rid of all alcohol spillages. Put a 
                    good amount onto the stain and pull the fabric tight and pour boiling water on it. Keep it on the side for a couple of hours and then wash it as 
                    normal in the washing machine.</p>
                    <p class="black-text"><strong>Makeup</strong></p>
                    <p>How annoying is it when you go to try on new clothes and there’s a foundation mark on the collar? Or perhaps you’re trying to not get makeup 
                    on your friend’s mum’s white top when she greets you with a hug. Strangely shaving cream and a sponge can remove any liquid makeup from garments. 
                    Who knew hey? You can also use hairspray to remove lipstick staining. You’re welcome!</p>
                    <p class="black-text"><strong>Sweat Stains</strong></p>
                    <p>Sweat stains happen to everyone, and it’s an embarrassing stain we like to hide as much as possible. Have you ever kept a jacket on just because 
                    of it? No just me? You can prevent that yellowy discolouration by spraying your tops with lemon juice before washing them. </p>
                    <p class="black-text"><strong>Grass Stains</strong></p>
                    <p>If you’re doing a sports degree then chances are your kit will be covered in grass stains. Lucky you! Get plenty of toothpaste and brushes when 
                    they’re on offer in Savers, because a blob of toothpaste (not gel) and a dip of your toothbrush in luke warm water, will scrub away any discolouration. </p>
                    <p class="black-text"><strong>Tea Stains</strong></p>
                    <p>If you’re a biscuit dunker like me, then you’ll know how hard it is to not spill tea (chocolate digestives being my fave). So remove those splash stains 
                    by getting a glass of water and stir sugar into it until it dissolves. If you fill it up with warm water that will be easier to dissolve the sugar crystals. 
                    Cover the stain with the solution, leave it for ten minutes and then put it in the laundry. </p>
                </div>
                <div class="tip">
                    <p class="tip-heading">Top Tip!</p>
                    <p>Once stained, act straight away. The longer the stain the tougher it is to get rid of. </p>
                    <p>Always try and get a stain out before putting it in the wash.</p>
                    <p>Don’t rub it, blot it gently so you don’t spread it.</p>
