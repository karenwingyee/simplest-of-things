                    <h2>How to Make Fried Eggs</h2>
                    <p>Fried eggs are the best when they’re accompanied with bacon and bread. Mmmm!</p>
                    <div class="big-block">
                        <p class="black-text"><strong>Ingredients:</strong></p>
                        <ul>
                            <li>Large egg </li>
                            <li>Cooking Oil </li>
                        </ul>
                        <p class="black-text"><strong>Method:</strong></p>
                        <ol>
                            <li>Put a small amount of oil in a frying pan and place on a low heat.</li>
                            <li>Crack the egg, being aware of the shell and to not break the yolk. </li>
                            <li>Keep an eye on it and cook for around 5 minutes.</li>
                            <li>It’s done, when there’s white around the edges. </li>
                        </ol>
