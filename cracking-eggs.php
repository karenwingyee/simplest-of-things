                    <h2>How to Make Poached Eggs</h2>
                    <p>The best way to do poached eggs is to buy an egg poacher. You can get this practically anywhere and at an affordable price too. Here’s a really easy step by step to get the perfect poached egg with a runny yolk. </p>
                    <div class="big-block">
                    <p class="black-text"><strong>Ingredients: </strong></p>
                    <ul>
                        <li>2 large eggs</li>
                        <li>Cooking oil</li>
                        <li>Egg Poacher </li>
                    </ul>
                    <p class="black-text"><strong>Method:</strong></p>
                    <ol>
                        <li>In your egg poacher you’ll find cups, you want to remove these. </li>
                        <li>Then fill the pan up with about an inch of water and bring it to the boil on a low heat.</li>
                        <li>Add a little cooking oil to a piece of kitchen roll and spread around each cup you’re filling.</li>
                        <li>Crack each egg into a cup, and place it back into the poacher facing upwards. </li>
                        <li>Cover the pan with the lid provided and cook for 4 minutes on a low heat still.</li>
                        <li>Once cooked, remove the cups and place the eggs on a plate. </li>
                    </ol>
