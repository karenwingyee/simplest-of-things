                    <h2>How to Make Scrambled Eggs</h2>
                    <div class="big-block">
                        <p class="black-text"><strong>Ingredients:</strong></p>
                        <ul>
                            <li>2 large eggs </li>
                            <li>Salt and pepper</li>
                            <li>A knob of butter</li>
                        </ul>
                        <p class="black-text"><strong>Method:</strong></p>
                        <ol>
                            <li>Crack the eggs into a medium sized bowl or a measuring jug. </li>
                            <li>Using a fork beat the eggs well in the bowl. </li>
                            <li>Season it to taste using salt and pepper.</li>
                            <li>Get a saucepan and put it on a low heat (2-3) and add the butter.</li>
                            <li>Leave the butter to melt and make sure that it doesn’t bubble.</li>
                            <li>Once it’s all melted, pour your egg mixture in.</li>
                            <li>Keeping it on a low heat, stir the butter and the eggs together with a wooden spoon, and keep stirring every so often so the egg doesn’t stick to the bottom of the pan.</li>
                            <li>You’ll soon find that the eggs come together and become scrambled and fluffy. Take it off the heat and there you have it. </li>
                            <li>Serve it with a couple of slices of toast. </li>
                        </ol>
                    </div>
                    <div class="tip">
                        <p class="tip-heading">Did You Know...?</p>
                        <p>You can make scrambled eggs in the microwave! That’s right, save the washing up (because I know how much you hate it) and use just one bowl to get the same results. Gino D'acampo swears by it! </p>
                        <ol>
                            <li>You need to use a microwave-proof container and begin by putting a tsp of cooking oil in it.</li>
                            <li>Add your eggs into the container and whisk them with a fork. Add your milk and season with salt and pepper. </li>
                            <li>Cover with cling film and make a small vent. Pop in the microwave on a medium to high power for 1 minute and then give it a stir. Then do it for a further minute. Keep doing this for a minute at a time until they’re how you want them. </li>
                            <li>Take them out and let them stand covered for a minute. Then enjoy!  </li>
                        </ol>
