                <p><strong>Rare:</strong> 2 ¼ minutes each side</p>
                <p><strong>Medium-rare:</strong> Around 3 ¼ on one side, and then repeat on the other.</p>
                <p><strong>Medium:</strong> About 4.5 minutes each side</p>
            </div>
