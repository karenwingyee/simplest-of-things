                <blockquote>You need to get yourself an ironing board and an iron. Now I’m not an expert on different models of irons, and I certainly don’t use all the features but the Tesco basics range which I have, works a treat for me. My ironing board is a mini version and is from IKEA, this way it fits perfectly in the corner of your room. 
                <p>My tip is to set yourself up infront of the TV or have Netflix on your laptop, because this then turns a boring chore into something a bit more bearable. </p>
                </blockquote>
                <div class="big-block">
                    <p><strong class="black-text">Step 1:</strong> On your iron you will find a small hole near the top of the handle which you have to fill with water to get everything up 
                    and running. My top tip is to have a plastic bottle nearby, this way it makes pouring the water an easier process, thanks to the narrow neck of the bottle.</p>
                    <p><strong class="black-text">Step 2:</strong> Fill the water up to the line on your iron and plug it in. Do not overfill your iron! I don’t know what happens when you 
                    do but I don’t think it’s worth finding out. Your iron will have a number of different dials and settings, so I suggest just keeping it on the ‘cotton’ 
                    setting.</p>
                    <p><strong class="black-text">Step 3:</strong> I advise turning the temperature up when ironing thicker clothes such as denim jeans, and lower for thinner and more delicate 
                    fabrics like blouses. </p>
                    <p><strong class="black-text">Step 4:</strong> Now iron away. If you have any plastic logos be extremely careful. I suggest putting a tea towel over it otherwise your 
                    logos will melt and will be transferred onto the iron (no one wants extra mess to clear up). </p>
                    <p><strong class="black-text">Step 5:</strong> There you go, hopefully you’ve got a nice crease-free shirt or dress. Simply unplug it and let it cool upwards before packing 
                    it away.</p>
                </div>
                <div class="tip">
                    <p class="tip-heading">Did You Know...?</p>
                    <p>Haven’t got time for ironing? Hang something up on a hanger in your bathroom after a shower and the steam will get the creases out. </p>
                    <p>Use straighteners to iron clothing. Just make sure that the plates are hair-free and not sticky and then put your straighteners on a low heat.</p>
