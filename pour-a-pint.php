            <div class="big-block">
            <p><strong class="black-text">Step 1</strong></p>
            <p>Get your beer and a clean pint glass. Then tilt the glass at a suggested 45 degree angle. Let the beer pour freely and try to hit the centre of the glass. 
            By doing this, it stops the beer getting too much head. </p>
            <p><strong class="black-text">Step 2</strong></p>
            <p>Stop pouring when the glass is about 2/3 of the way full, and hold it up right and pour the rest in. You’re aiming for about half an inch of head, as then 
            you won’t lose the flavours of the drink. </p>
