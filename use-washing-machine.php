            <h3>How to Use a Washing Machine</h3>
            <img class="wide-img" src="img/use-washing-machine.png" alt="How to Use a Washing Machine" title="How to Use a Washing Machine">
            <div class="intro-block">
                    <p>For me, learning how to work a washing machine was more scary than actually going to university. You’ve heard the horror stories of how your friend has shrunk their expensive Abercrombie and Fitch clothes, and their white Calvin’s are now a baby pink. </p>
                    <p>Follow my step by step and you’ll have nothing to worry about chuck! </p>
                </div>
            <div class="inner">
                <div class="big-block">
                    <p><strong class="black-text">Step 1:</strong> Get your laundry basket and separate your clothes into two piles of colours and whites. You don’t want to mix them otherwise your clothes could be stained from running colours. </p>
                    <p><strong class="black-text">Step 2:</strong> A tip from my mother (Hi Anne). Make sure you read the labels. Some things will need to be hand washed or have a special treatment. I think that’s why I buy all my clothes from Primarni, saves me hassle when it comes to washing them..</p>
                    <p><strong class="black-text">Step 3:</strong> Turn all clothes inside out because then it stops logos fading. </p>
                    <p><strong class="black-text">Step 4:</strong> Load your clothes into the washing machine and get a washing powder scoop and fill it half way up.</p>
                    <p><strong class="black-text">Step 5:</strong> Select the temperature to 40 degrees as that’s your standard washing temperature, and press start. If you’re wanting to wash things on a cooler wash then set it to 30 degrees, or a higher wash for towels and bedding. </p>
                    <p><strong class="black-text">Step 6:</strong> You’ll see how long each wash takes as it will say on the machine. When done put it all on an airer. Or hang it all on the back of your door STUDENT STYLE. </p>
                </div>
                <div class="tip">
                    <p class="tip-heading">Top Tip!</p>
                    <p>Have colour catches at hand </p>
                    <p>Don’t do washing when you’re drunk.</p>
                    <p>If in doubt do it on a cooler wash</p>
                    <p>Don’t use a tumble dryer to dry clothes they’ll shrink, leave it to bed sheets. </p>
                    <p>Got something new? Wash it on its own so the colour doesn’t run. </p>
