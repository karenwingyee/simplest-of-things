                <div class="big-block">
                    <p><strong class="black-text">Step 1: </strong>Crack the two eggs into a mixing bowl and season with your salt and pepper. Get your fork and beat well, ensuring that there’s no shell. </p>
                    <p><strong class="black-text">Step 2: </strong>Get a frying pan, spray some frylight and put it on a low heat. Once it’s heated up, add the butter.</p>
                    <p><strong class="black-text">Step 3: </strong>Once it’s all melted, add the eggs and make sure you spread them around the pan evenly by moving it gently. The timing can be 
                    tricky at first for beginners because you want the omelette to be slightly cooked, but so that the egg is still a little raw on top. </p>
                    <p><strong class="black-text">Step 4: </strong>Add your filling, so in this case I’m going to sprinkle my cheese and ham. Then you want to get a spatula and carefully go around 
                    the edge and fold it in half. </p>
                    <p><strong class="black-text">Step 5: </strong>Carry on cooking it on a low heat until the bottom is golden brown, and then serve it. </p>
                </div>
