            <div class="big-block">
                <p class="black-text"><strong>What you will need: </strong></p> 
                <ul>
                    <li>Matches</li>
                    <li>Tinder: wood, paper, leaves, bark and dead plants.</li>
                    <li>Kindling: dry twigs, large wood pieces. </li>
                </ul>
                <p class="black-text"><strong>Method: </strong></p> 
                <ol>
                    <li>Before you start your fire, you need to make sure that you have a cleared area. You don’t want to be making a fire on top of grass, or around anything which could catch fire. </li>
                    <li>Grab your tinder and put it in a bundle where you want it. </li>
                    <li>You now want to get your kindling and form a tepee over the tinder. Use the thicker and bigger parts of the tinder to create the tepee. Followed by the smaller and thinner bits to go on top and next to it.  </li>
                    
                    <li>You then want to make a small gap in your tepee, the side which the wind is blowing against, so that the fire has the air it needs to get going and to reach the kindling.</li>
                    <li>Light a match and carefully put it under your tinder. The structure should hopefully fall after time, and once it has a decent sized flame, feel free to add more logs to the fire.</li>
                </ol>
            </div>
            <div class="tip">
                <p class="tip-heading">Top Tip!</p>
                <p>Blow over the fire gently to get it going.</p>
