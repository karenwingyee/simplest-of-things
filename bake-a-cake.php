                <blockquote>
                As a student who absolutely loves to bake, I understand how it can be quite an expensive hobby so I try and keep it simple by sticking to a few basic recipes that are easy to remember as well as easily adaptable!
                <p>Baking isn’t difficult, however it is a science so precision is key.</p>
                <p>When I first started to bake I didn’t understand this and thought by chucking everything into a bowl and whisking it up it’d turn into a beautiful cake. How wrong I was.</p>

                <p>9 times out of 10 a recipe goes wrong because it was not followed correctly. To avoid kitchen disaster, read the recipe through before you start, check you have 
                the correct ingredients and get them prepared before so. </p>

                <p>Here is my recipe for a simple Victoria Sponge which you can adapt very easily. Make it extra indulgent by filling it with whipped cream and strawberries, add 
                lemon zest to the batter and fill with lemon curd to make it summery or just stick to the simple jam and buttercream filling.</p>
                </blockquote>
                
                <div class="big-block">
                <p class="black-text"><strong>Ingredients:</strong></p>
                <ul>
                    <li>200g softened butter</li>
                    <li>200g caster sugar</li>
                    <li>4 eggs</li>
                    <li>1 tsp vanilla extract</li>
                    <li>200g plain flour</li>
                    <li>2 tsp baking powder</li>
                    <li>300 ml double cream</li>
                    <li>Fresh chopped strawberries</li>
                </ul>
                <p class="black-text"><strong>Equipment:</strong></p>
                <ul>
                    <li>2 sandwich tins </li>
                    <li>Electric beaters or wooden spoon </li>
                    <li>Scales</li>
                    <li>Large Bowl</li>
                </ul>
                <p class="black-text"><strong>Method:</strong></p>
                <ol>
                    <li>Preheat oven to 180&deg;c and grease and line 2 sandwich tins with baking paper</li>
                    <li>Cream butter and sugar together for at least 5 minutes.</li>
                    <li>Add in two eggs and then half the flour. Beat well</li>
                    <li>Add the vanilla, the last 2 eggs, flour and baking powder and beat until combined</li>
                    <li>Divide the mixture between the two tins and cook in the oven for 30 minutes</li>
                    <li>Leave to cool.</li>
                    <li>Whip up the cream and dollop onto one side of the sponge. Scatter with strawberries.</li>
                    <li>Dust with icing sugar.</li>
                </ol>
                <p>Enjoy!</p>
                </div>
                <div class="tip">
                    <p class="tip-heading">Top Tip!</p>
                    <p>Push your index finger gently into the sponge, if it leaves a mark it needs longer. If it springs back up then it’s perfectly cooked. </p>
                    <p>You need to use two different types of sugar, Caster and Icing don’t confuse them both.</p>
                    <p>Don’t want to use baking paper for the tins? Get some kitchen roll and a bit of butter and grease your tins that way. </p>
