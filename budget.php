				<blockquote>If you're lucky enough to be a carefree student, then up to now you might not have given much thought to where your money goes. But whether your priorities are beer, books, or baked beans, or whether your situation dictates a totally different set of priorities, it's important to set a budget, and stick to it.</blockquote>

				<div class="big-block">
					<p class="black-text"><strong>Set a budget</strong></p>
					<ol>
						<li>Gather up any information you have about your income and expenditure</li>
						<li>Add together all your income, making sure the payment frequency is the same for all (eg. weekly or monthly)</li>
						<li>Write down broad categories of spending, then break these down into specifics. </li>
					</ol>
					<p>You'll need to know how much money you have coming in - student loans, grants and any other income - and what your essential spends are, like tuition 
					fees, rent, travel, food, and bills. Work out how much money is left over and decide which of your non-essential spends (like new clothes and nights out) 
					you can afford, and which you'll live without.</p>

					<p class="black-text"><strong>Stick to it</strong></p>
					<p>Setting your budget is one thing (and it's actually quite liberating!) but sticking to it, is quite another. If you've been used to spending the student 
					loan as soon as it hits your bank account, it can take a real mindset shift to change your ways.</p>

					<p class="black-text"><strong>Before you buy</strong></p>
					<ol>
						<li>Think about whether you've planned for the purchase, or whether it's an impulse buy. </li>
						<li>Consider whether it is something you need or something you want, and how useful it would be.</li>
						<li>Think about whether you can really afford it.</li>
						<li>Price around - the internet makes this easy peasy! </li>
						<li>Check out product reviews. It's a waste of money if it's not fit for purpose.</li>
					</ol>

					<p class="black-text"><strong>After you buy</strong></p>
					<ol>
						<li>If in any doubt about something you've bought - take it back for a refund within the returns period. There's no point in keeping something you're not 
						sure you'll ever use.</li>
						<li>Keep an eye on the amount spent, to make sure you stay within budget.</li>
						<li>Learn from your spending habits, and make small changes. Try to spend a little less, or save a little more, each month. Rome wasn't built in a day and 
						old habits take time to change, so give yourself a break!‚Äù</li>
					</ol>
				</div>
				<div class="tip">
					<p class="tip-heading">Top Tip!</p>
					<p>Keep your receipts</p>
					<p>Write down everything you spend.</p>
