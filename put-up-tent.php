            <h3>How to Put Up a Tent</h3>
            <img src="img/tent.png" alt="How to Put Up a Tent" title="How to Put Up a Tent">
            <div class="intro-block">
                <p>The beauty about university, is that you do a lot of half days. This means you have plenty of time to have a social life. This could mean going to festivals or to go camping for a few days with your new mates. </p>
                <p>So to save yourself from embarrassment, check out the steps below, memorise them like a boss and voila! In no time at all you’ll be roasting marshmallows outside your tent. </p>
            </div>
            <div class="inner">
                <div class="big-block">
                    <p class="black-text"><strong>What you will need:</strong></p>
                    <ul>
                        <li>Tent</li>
                        <li>A Tarp which is the same size as the tent</li>
                    </ul>
                    <p class="black-text"><strong>Method:</strong></p>
                    <p><strong>Step 1:</strong> Find an area which is level and is stone and twig-free. Get your tarp, lie it down and unfold your tent on top of it. This way it keeps it clean. Your tent also doesn’t get as wet if you are camping in festival weather and mud. </p>
                    <p><strong>Step 2:</strong> Get your tent poles and connect them together. You’ll then see some sleeve-like holes along the narrow seams where you need to insert the poles. Once you’ve connected them together, the ends will go into eyelets around the base of the tent, securing its shape. They should bend and make an arch for your tent. </p>
                    <p><strong>Step 3:</strong> Grab one end of the tent and begin to pull it upright. Take one of the included pegs and secure each end into the ground. The idea is to expand the tent into a more tent-like shape.  </p>
                    <p><strong>Step 4:</strong> Next up, take the remaining pegs and make your way around the outside, putting the pegs through the peg holes. Ensure that any tarp flooring is secure and folded under so you don’t end up with a soggy bottom. </p>
                    <p><strong>Step 5:</strong> Nearly done! Time to put in your inner tent. While each tent differs from the next, you should be able to follow the line of toggles on your inner tent and place them through the loops which are sewn into the inside of the main tent. Start toggling from the central toggle, usually identifiable by its colour. Good luck!</p>
                </div>
