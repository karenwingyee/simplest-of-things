                <h2>Treat a Hangover</h2>
                <div class="big-block">
                    <p class="black-text"><strong>Water (boring but true)</strong></p>
                    <p>I’m sure you’ve heard people say that if you want to be hangover-free you should drink a glass of water between your drinks. That’s because when 
                    you drink alcohol you lose up to four times as much water. This then results in the body becoming dehydrated which = hangover from hell! .</p>
                    <p>Your body ends up not absorbing the water, and sends it straight to your bladder instead; which explains the many god damn toilet breaks you have throughout the night. I hate ‘breaking the seal’, especially in a jumpsuit! </p>
                    <p>So they suggest drinking up to 8 pints of water the following day to help the body feel back to its normal self, so it doesn’t escalate to the mother of all migraines. </p>
                    
                    <p class="black-text"><strong>Fruit Juice</strong></p>
                    <p>Make sure you stock up on a couple of cartons of fruit juice in your fridge because it can really help you the next day. My faves being pineapple and orange juice. I usually close my eyes when sipping mine and pretend I’m on a tropical island to block out the horrific hangover. Apparently the sweet fruitiness and vitamins injects energy, and gives you the boost you need to say goodbye to those alcohol toxins. </p>
                    
                    <p class="black-text"><strong>Coconut Water </strong></p>
                    <p>If you find drinking water a struggle, then why not give coconut water a go. Not only does it add more flavour, to make it that little bit more exciting, many people swear by it! It’s insanely hydrating, which is why I think many people drink it on those desert island TV shows. It also holds a ridiculous amount of potassium which will help your blood sugar levels. </p>
                    
                    <p class="black-text"><strong>Grass Stains</strong></p>
                    <p>If you’re doing a sports degree then chances are your kit will be covered in grass stains. Lucky you! Get plenty of toothpaste and brushes when 
                    they’re on offer in Savers, because a blob of toothpaste (not gel) and a dip of your toothbrush in luke warm water, will scrub away any discolouration. </p>
                    
                    <p class="black-text"><strong>Ginger and Peppermint Tea</strong></p>
                    <p>I totally understand that a nice milky cuppa sounds more appealing than an old fashioned flavoured tea, but it really will help soothe the body. There’s nothing worse than doubling over all day feeling sick, when you just can’t budge it. Ginger and peppermint tea really helps sickness and nausea. </p>
                    
                    <p class="black-text"><strong>Bananas</strong></p>
                    <p>Like coconut water, bananas are full of potassium and so they’re ideal to eat in the morning when you can’t stomach anything else. They will restore and soothe the body back to normal after a heavy night out. </p>
                    
                    <p class="black-text"><strong>Eggs</strong></p>
                    <p>After all the battering you gave your liver last night with those 241 cocktails and the many shots, it sure needs some TLC. Eggs are a great source for this because the amino acid taurine and cysteine helps boots your liver and removes any yuckiness. So go ahead and have that fried egg sandwich.   </p>

                    <p class="black-text"><strong>Tomatoes</strong></p>
                    <p>I know it’s hard but just pretend your tomato juice is a Bloody Mary or a fruity Woo Woo. Tomatoes are full of vitamin C and will give the liver a boost
                     and reduce inflammation. </p>
                    <p class="black-text"><strong>Crackers</strong></p>
                    <p>It may be like chewing on cardboard, but apparently they’re loaded with preservatives and therefore are the thing to nibble on. If you want to make them 
                    a little more bearable then add a little bit of honey on top, which apparently helps burn the alcohol off quicker. </p>

                    <p class="black-text"><strong>Asparagus </strong></p>
                    <p>So you could be mistaken for a rabbit if you suddenly whacked out some asparagus from your handbag after a night out, but scientists have proven that it 
                    helps break down the alcohol in the body. </p>
                </div>
                
                <h2>What should you AVOID?</h2>
                <div class="big-block">
                    <p class="black-text"><strong>Hair of the Dog’</strong></p>
                    <p>Otherwise known as drinking alcohol the following day. It’s like when you buy a Chinese and it tastes so much better the next day. A lot of people think 
                    this will “take the edge” off the next day. WRONG!</p>
                    <p>By doing this you’ll only get more dehydrated, which then results to a bigger headache. Carry a bottle of water around with you and sip on that throughout 
                    the day. </p>

                    <p class="black-text"><strong>Greasy food</strong></p> 
                    <p>Trust me I don’t want to say this either, but replace your typical cooked breakfasts and your butties with fruit and smoothies. Easier said than done I 
                    know, and I’m still guilty of consuming a rasher or two, it’s just so good. Greasy food can irritate your stomach, giving you multiple trips to the bathrooms 
                    and making you feel worse later that day. </p>
                    <p class="black-text"><strong>Coffee </strong></p>
                    <p>I know what you’re thinking, coffee in the “not to drink” list?! Coffee contributes to your hangover and so you want to drink fruit tea because it’s a 
                    diuretic which means it makes you go to the toilet more. By doing this you are dehydrating your body more and just making your headache ten times worse. </p>
